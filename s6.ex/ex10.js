// 10) Crie uma função que verifica se um número inteiro passado como parêmetro é divisível por 3 e retorne true ou false.

function verificarNumero(numero) {
   return numero % 3 === 0
}

console.log(verificarNumero(3))
console.log(verificarNumero(4))
console.log(verificarNumero(5))
console.log(verificarNumero(6))
